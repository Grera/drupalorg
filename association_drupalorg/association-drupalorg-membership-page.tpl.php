<h2>What else can I do?</h2>
<p>If you would like to give the association more than the membership fee you can also make a donation, offer to be a sponsor for Drupalcon, or sponsor a local Drupal event.  If you choose not to purchase an individual or organizational membership  you can support the Drupal project by <a href="/donate">donating to the Drupal Association</a>.</p>

