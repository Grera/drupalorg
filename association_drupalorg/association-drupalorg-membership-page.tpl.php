<h2>What is Drupal Association membership?</h2>
<p>The Drupal Association can not take credit for the amazing success of the Drupal project, but will highlight the success of the community in its efforts to keep supporting the project. We are supported by Individuals and Organizations who become yearly members. Our membership makes the Drupal.org infrastructure possible and supports <a href="/projects">community initiatives</a>. Organization Membership information is below the Individual Memberships, scroll down to see.</p>

