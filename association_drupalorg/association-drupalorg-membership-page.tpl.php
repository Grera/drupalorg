<p>To join the Drupal Association, you must first be signed in to Drupal.org</p>

<p>Purchase or Renew a Drupal Association Membership:</p>
<ul>
  <li><a href="/civicrm/contribute/transact?reset=1&id=1">Buy an Individual Membership</a> for €22, approx $30.</li>
  <li><a href="/civicrm/contribute/transact?reset=1&id=2">Buy an Organization Membership</a> for €73, approx $101.</li>
</ul>

<h2>Membership Benefits</h2>

<h3>1. Membership Directory Listing</h3>
<p>Members are listed in the public membership directories. Members who join or renew are listed in the Drupal Association's annual report.</p>
<p><a href= "/civicrm/profile?reset=1&force=1&gid=8&search=0"> Directory of Individual Memberships</a></p>
<p><a href= "/civicrm/profile?reset=1&force=1&gid=4&search=0"> Directory of Organization Memberships</a></p>

<h3>2. Membership Badges</h3>
<p>Members are granted free use of the Drupal Association Membership Badges while they remain active members. Members can put it on their websites, blogs, etc. A badge will also show in their Drupal.org user profile.</p>

<h3>3. Drupal Marketplace</h3>
<p>Organization Members who have contributed to Drupal can be listed in the <a href="http://drupal.org/marketplace-preview" alt="Drupal Marketplace">Drupal Marketplace</a>.</p>

